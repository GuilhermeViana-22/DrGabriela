		$(document).ready(function() {
			$('#submit').click(function() {

				var nome = $('#nome').val();
				var email = $('#email').val();
				var subject = $('#subject').val();
				var message = $('#message').val();

				$('#alert').html('');
				if (nome == '') {
					$('#alert').html('Prencha seu nome');
					$('#alert').addClass("alert-danger");

					return false;
				}
				$('#alert').html('');

				if (email == '') {
					$('#alert').html('Prencha o email');
					$('#alert').addClass("alert-danger");

					return false;
				}

				$('#alert').html('');
				if (subject == '') {
					$('#alert').html('Prencha o assunto');
					$('#alert').addClass("alert-danger");

					return false;
				}

				$('#alert').html('');
				if (message == '') {
					$('#alert').html('Prencha a messagem');
					$('#alert').addClass("alert-danger");

					return false;
				}
				$('#alert').html('');

				$.ajax({
					url: 'enviar.php',
					method: 'POST',
					data: {
						nome: nome,
						email: email,
						subject: subject,
						message: message
					},
					success: function(result) {
						$('form').trigger("reset");
						$('#alert').addClass("alert-success");
						$('#alert').fadeIn().html(result);
						setTimeout(function() {
							$('#alert').fadeOut('Slow')
						}, 3000)
					}
				});
			})
		});
